    private void jBotonAñadirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBotonAñadirActionPerformed
        // TODO add your handling code here:
        
        try {
            c1 = Integer.parseInt(jTextFieldCiudadUnion1.getText());
            c2 = Integer.parseInt(jTextFieldCiudadUnion2.getText());
            tamaño = Double.parseDouble(jTextFieldAristaValor.getText());
            
            
            if(!as.isEmpty()) {
                int inX = as.SearchIndex(c1 - 1, c2 - 1);
                Arista al = as.getArista(inX);
                if(c1 == al.src + 1 && c2 == al.dst + 1) {
                    return;
                }
                
            }
            
            
            
            /*
            if(c1 == Integer.parseInt(temp1) && c2 == Integer.parseInt(temp2)) {
                return;
            }
            */

            if(tamaño < 1) {
                JOptionPane.showMessageDialog(null, "Error al añadir arista: La distancia debe de ser mayor.");
                return;
            }

            if(c1 != c2 && c1 < nCiudades && c2 < nCiudades) {
                as.InsertLast(c1 - 1, c2 - 1, tamaño);
                mt.addRow(new Object[] {c1, c2, tamaño});
            }
            else {
                JOptionPane.showMessageDialog(null, "Error al añadir arista: Asegúrese de que los datos sean coherentes.");
            }
        }
        catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error al añadir arista: Asegúrese de que haya introducido los datos.");
        }
        
    }//GEN-LAST:event_jBotonAñadirActionPerformed

