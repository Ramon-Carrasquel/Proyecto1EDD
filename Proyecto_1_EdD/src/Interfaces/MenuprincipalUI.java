    private void jBotonEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBotonEliminarActionPerformed
        // TODO add your handling code here:
        
        try {
            int row = jTable1.getSelectedRow();
            String temp1 = jTable1.getModel().getValueAt(row, 0).toString();
            String temp2 = jTable1.getModel().getValueAt(row, 1).toString();

            c1 = Integer.parseInt(temp1);
            c2 = Integer.parseInt(temp2);

            int inX = as.SearchIndex(c1 - 1, c2 - 1);
            as.Delete(inX - 1);

            mt.removeRow(jTable1.getSelectedRow());
        }
        catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error al eliminar arista: No se ha seleccionado una.");
        }
    }//GEN-LAST:event_jBotonEliminarActionPerformed

